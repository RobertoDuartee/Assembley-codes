.INCLUDE "m16def.inc"
.ORG 0
RJMP MAIN
.ORG 0x26
RJMP TIMER0

MAIN:


AGAIN:
CALL TECLADO
CPI R19,10
BRNE AGAIN

AGAIN2:
CALL TECLADO
CPI R19,10
BRLO ROT
RJMP ENTER

ROT:
LDS R16,0x61
STS 0X60,R16
LDS R16,0X62
STS 0X61,R16
LDS R16,0x63
STS 0X62,R16
STS 0X63,R16
RJMP AGAIN2

ENTER:
CPI R19,11
BREQ CONTINUA
RJMP AGAIN2

CONTINUA:

FIN: RJMP FIN